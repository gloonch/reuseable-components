const {Schema, model, mongoose} = require('mongoose');


const UserSchema= new Schema(
    {
        name: {
            type: String,
            required: true,
        },
        email: {
            type: String,
            required: true,
            unique: true
        },
        password: {
            type: String,
            required: true,
            minlength: 6
        },
    });

module.exports = model("User", UserSchema);